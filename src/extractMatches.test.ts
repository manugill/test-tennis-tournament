import {
  assertEquals,
  assertThrows,
} from "https://deno.land/std/testing/asserts.ts";
import extractMatches from "./extractMatches.ts";

Deno.test("should fail for empty text", () => {
  assertThrows(
    () => {
      extractMatches("");
    },
    undefined,
    "No matches were found",
  );
});

Deno.test("should fail if match doesn't define players", async () => {
  assertThrows(
    () => {
      extractMatches(`
        Match: Test
        0
        1
    `);
    },
    undefined,
    "Invalid format, players should be defined before scores",
  );
});

Deno.test("should fail if match defines invalid scores", async () => {
  assertThrows(
    () => {
      extractMatches(`
        Match: Test
        Player 1 vs Player 2
        0
        1
        2
    `);
    },
    undefined,
    "Invalid score, only 0 and 1 are valid values",
  );
});

